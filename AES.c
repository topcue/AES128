uchar** gen_Matrix()
{
	uchar** X = NULL;
	
	X = (uchar**)calloc(4, sizeof(uchar*));
	for (int i = 0; i < 4; i++)
		X[i] = (uchar*)calloc(4, sizeof(uchar));
	return X;
}

void Show_Matrix(uchar** X)
{
	for (int i = 0; i < 4; i++)
		for (int j = 0; j < 4; j++)
			printf("%02X ", X[j][i]);
	printf("\n");
}

void Free_Matrix(uchar** X)
{
	for (int i = 0; i < 4; i++)
	{
		free(X[i]);
	}
	free(X);
}

void Copy_Matrix(uchar** src, uchar** dst)
{
	for (int i = 0; i < 4; i++)
	{
		for (int j = 0; j < 4; j++)
		{
			src[i][j] = dst[i][j];
		}
	}
}

void SubBytes(uchar** X)
{
	uchar tmp;
	int i = 0, j = 0;
	for (i = 0; i < 4; i++)
		for (j = 0; j < 4; j++)
		{
			tmp = X[i][j];
			X[i][j] = S_Box[16 * (tmp >> 4) + (tmp & 0x0f)];
		}
}

void ShiftRows(uchar** X)
{
	uchar tmp = 0x00;
	tmp = X[1][2];	X[1][2] = X[1][3];	X[1][3] = X[1][0];	X[1][0] = X[1][1];	X[1][1] = tmp;
	tmp = X[2][0];	X[2][0] = X[2][2];	X[2][2] = tmp;
	tmp = X[2][1];	X[2][1] = X[2][3];	X[2][3] = tmp;
	tmp = X[3][0];	X[3][0] = X[3][3];	X[3][3] = X[3][2];	X[3][2] = X[3][1];	X[3][1] = tmp;
}

void xtime(uchar* x)
{
	*x = (*x << 1) ^ (((*x & 0x80) * 0x1B) >> 7);	
}

void MixColumns(uchar** X)
{
	int i = 0;
	uchar a = 0, x_0_i = 0, Tmp = 0;
	for (i = 0; i < 4; i++)
	{
		Tmp = X[0][i] ^ X[1][i] ^ X[2][i] ^ X[3][i];	x_0_i = X[0][i];
		a = X[0][i] ^ X[1][i];	xtime(&a);	X[0][i] ^= a ^ Tmp;
		a = X[1][i] ^ X[2][i];	xtime(&a);	X[1][i] ^= a ^ Tmp;
		a = X[2][i] ^ X[3][i];	xtime(&a);	X[2][i] ^= a ^ Tmp;
		a = X[3][i] ^ x_0_i;	xtime(&a);	X[3][i] ^= a ^ Tmp;
	}
}

void KeySchedule(uchar** X, int R)
{
	uchar a[4] = {0x00, };
	uchar tmp = 0;
	int i = 0, j = 0;

	uchar Temp[16] = {0x00, };

	for (j = 0; j < 4; j++)
		for (i = 0; i < 4; i++)
			Temp[i * 4 + j] = X[i][j];

	a[0] = X[0][3];	a[1] = X[1][3];	a[2] = X[2][3];	a[3] = X[3][3];	tmp = a[0];
	a[0] = a[1];	a[1] = a[2];	a[2] = a[3];	a[3] = tmp;

	for (i = 0; i < 4; i++)
	{
		a[i] = S_Box[16 * (a[i] >> 4) + (a[i] & 0x0F)];
		a[i] ^= Rcon[R * 4 + i];
		Temp[i] = a[i] ^ X[i][0];
	}

	for (j = 1; j < 4; j++)
		for (i = 0; i < 4; i++)
			Temp[j * 4 + i] = X[i][j] ^ Temp[j * 4 + i - 4];
	
	for (i = 0; i < 4; i++)
		for (j = 0; j < 4; j++)
			X[j][i] = Temp[i * 4 + j];
}

void AddRoundKey(uchar** X, uchar** K)
{
	int i = 0, j = 0;
	for (i = 0; i < 4; i++)
		for (j = 0; j < 4; j++)
			X[i][j] ^= K[i][j];
}

